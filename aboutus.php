<h2>Academics</h2>
<hr>
<p>
MNNIT offers a four-year undergraduate course in various engineering disciplines. Indian students are admitted though Joint Entrance Exam (JEE Mains), while foreign students are admitted through Direct Admission of Students Abroad (DASA).<br><br>

MNNIT offers 19 post-graduate programs of two-year duration leading to M.Tech. degrees.The Computer Science department offers a master's in Computer Applications (MCA). It has a master's course in Mathematics and Scientific Computing and a B-school known as the School of Management Studies. Master of Social Work (MSW) is also running in the institute since 2009.<br><br></p>
