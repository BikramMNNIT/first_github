<p>Motilal Nehru National Institute of Technology Allahabad (MNNIT or NIT Allahabad), formerly Motilal Nehru Regional Engineering College (MNREC), is a public higher education institute located in Prayagraj, Uttar Pradesh, India. It is one among the NIT's in the country, and like the rest of them, classified as an Institute of National Importance.The college has the distinction of the being first in the country to start an undergraduate programme in Computer Science & Engineering, in 1976-77</p>
<h2>History</h2>
<hr>
<p>Motilal Nehru Regional Engineering College was established in 1961. The foundation stone was laid by Jawaharlal Nehru, the first Prime Minister of India, and the institute was named after his father, lawyer and freedom fighter, Motilal Nehru. The founding principal of the college was Gopal Kishore Agrawal.

The main building of the college was inaugurated by Prime Minister Lal Bahadur Shastri on 18 April 1965. Effective from 26 June 2002, the college became a deemed university and was renamed as Motilal Nehru National Institute of Technology.<br><br>

From its beginnings the institute offered bachelor's degree programmes in Civil, Electrical and Mechanical Engineering. The institute was the first in the country to offer an undergraduate program in Computer Science and Engineering, which was started in 1976. In 1982-83 undergraduate programs in Electronics Engineering, and Production and Industrial Engineering were started.

Postgraduate programs were introduced in 1970-71 with master's degree courses in all engineering departments. The institute now offers M.Tech. degree programs MCA and M.Sc Mathematics and Scientific Computing course, and registers candidates for Ph. D. degrees. The institute has been recognized by the Government of India as a center for the quality improvement program in Civil and Mechanical Engineering. The institute started a two year/four semesters postgraduate degree in Management Studies in 1996. The institute added undergraduate programs in Chemical Engineering and BioTechnology in 2006.<br><br>

The college has been recognized by AICTE as a center for the Quality Improvement Programme. In 1972, the college established an industrial estate with 69 sheds with the objective of encouraging entrepreneurs and creating additional revenue.<br><br>


Design Centre<br>
The college is a member of the Indo-UK REC project in the area of design. Under this scheme, a Design Center was established at a cost of about Rs. six crore, which offers training and consultancy. It was given the status of a Institute of National Importance (INI) on 15 August 2007.<br><br>

In 2010, the institute celebrated its Golden Jubilee year.<br><br>

It is among the few technical institutions in India to house two supercomputers, PARAM 8000 and PARAM 10000.<br><br></p>
